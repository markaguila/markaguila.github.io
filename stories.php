<h3>Seneca Foods Moves to Open Computing Environment
</h3>
<p>
Business Need:<br>
In order to have a more open and flexible computing environment, the management of Seneca Foods Corporation decided to move from a Unisys ClearPath Mainframe computer using the DMSII database to an IBM AIX Unix server using the Oracle database.
<br>
The Order Entry system that was in use at Seneca Foods was custom written almost ten years previously using the 4GL LINC. Although the decision was made to change the hardware and the database, there was a desire to keep the software, which suited the business needs of the company very well. However, there were challenges:
</p>
<ul>
<li>
 The data needed to be converted and moved from the Unisys DMSII database to the Oracle database
</li>
<li>
The software needed to be ported from the Unisys MCP operating environment to the IBM AIX Unix operating environment

</li>
<li>
Unix scripts had to be developed from A Series WFLs and tested to control the running of batch programs
</li>
</ul>

<p>
New System Benefits:
<br>The challenge to move the Order Entry application to an open environment was met and the company realized several immediate benefits.
</p>
<ul>
<li>
 A Substantial improvement in performance and saving in runtimes over all applications was realized. The nightly offline process was reduced from 6 hours on the A Series to less than 1 hour.

</li>
<li>
 The need to train the user community in the use of a new Order Entry system was avoided since from the users’ standpoint, the Order Entry system was basically the same one that they had been using with the addition of a new, friendlier interface.

</li>
<li>
 Because the core application software had been retained, the IT staff did not need to be concerned about learning a new system but could concentrate on adding new features and functions as required by business needs.
</li>
<li>
 The Oracle database allowed both the user community and the IT staff to use readily available data query tools.
</li>
<li>
There was a substantial reduction in hardware maintenance costs.
</li>
<li>
The Unix environment allowed for the use of a variety of new development tools.

     </li>
</ul>


